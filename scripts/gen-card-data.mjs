// Generates src/game/cards/cardData.ts from data/cards_research.json (scraped wiki data).
// Run: node scripts/gen-card-data.mjs
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");

const sources = [
  { name: "cards_research.json", setId: "base" },
  { name: "cards_second_edition.json", setId: "base" },
  { name: "cards_adventures_second_edition.json", setId: "adventures-second-edition" },
];
const raw = sources.flatMap(({ name, setId }) =>
  JSON.parse(readFileSync(resolve(root, "data", name), "utf8")).map((card) => ({
    ...card,
    setId,
  })),
);

// Map the wiki "type" string to our internal CardKind union.
const KIND = {
  "Baby Unicorn": "baby",
  "Basic Unicorn": "basic",
  "Magical Unicorn": "magical",
  Magic: "magic",
  Upgrade: "upgrade",
  Downgrade: "downgrade",
  Instant: "instant",
};

function decodeEntities(s) {
  // The scrape left a couple of HTML numeric entities in flavor text.
  return s
    .replace(/&#x([0-9a-f]+);/gi, (_, h) => String.fromCodePoint(parseInt(h, 16)))
    .replace(/&#(\d+);/g, (_, d) => String.fromCodePoint(parseInt(d, 10)))
    .replace(/&amp;/g, "&")
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'")
    .replace(/\s+/g, " ")
    .trim();
}

const cards = raw
  .map((c) => ({
    slug: c.slug,
    name: c.name,
    kind: KIND[c.type],
    copies: c.copies,
    text: decodeEntities(c.text || ""),
    image: c.image,
    setId: c.setId,
  }))
  .sort((a, b) => a.slug.localeCompare(b.slug));

const total = cards.reduce((n, c) => n + c.copies, 0);

const header = `// AUTO-GENERATED by scripts/gen-card-data.mjs — do not edit by hand.
// Sources: ${sources.map(({ name }) => `data/${name}`).join(", ")}.
// ${cards.length} distinct cards, ${total} copies across the supported catalog.

export type CardKind =
  | "baby"
  | "basic"
  | "magical"
  | "magic"
  | "upgrade"
  | "downgrade"
  | "instant";

export interface CardData {
  /** Stable id used everywhere (definitions, art filenames, instances). */
  slug: string;
  name: string;
  kind: CardKind;
  /** Number of copies of this card in the base edition that contains it. */
  copies: number;
  /** Exact card text (2nd-edition wording) for display. */
  text: string;
  /** Public path to the card art. */
  image: string;
  /** Base deck or expansion catalog this definition belongs to. */
  setId: "base" | "adventures-second-edition";
}
`;

const body = `\nexport const CARD_DATA: CardData[] = ${JSON.stringify(cards, null, 2)};\n\nexport const CARD_DATA_BY_SLUG: Record<string, CardData> = Object.fromEntries(\n  CARD_DATA.map((c) => [c.slug, c]),\n);\n`;

writeFileSync(resolve(root, "src/game/cards/cardData.ts"), header + body);
console.log(`Wrote src/game/cards/cardData.ts: ${cards.length} cards, ${total} copies.`);
