// Generates the Unhinged Hedgehogs deck: one bespoke illustration per card
// (a real little scene, not a recolored silhouette) + the typed catalog.
//
// Art lives in scripts/hedgehog/scenes/*.mjs, composed from the character kit
// in scripts/hedgehog/kit.mjs. Names live in scripts/hedgehog/names.mjs.
//
//   pnpm gen-hedgehog   (or: node scripts/gen-hedgehog-deck.mjs)
import { mkdirSync, writeFileSync, rmSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { execFileSync } from "node:child_process";
import { fileURLToPath } from "node:url";

import { HEDGEHOG_NAMES } from "./hedgehog/names.mjs";
import { BABIES } from "./hedgehog/scenes/babies.mjs";
import { BASICS } from "./hedgehog/scenes/basics.mjs";
import { MAGICALS } from "./hedgehog/scenes/magicals.mjs";
import { MAGICALS2 } from "./hedgehog/scenes/magicals2.mjs";
import { MAGIC } from "./hedgehog/scenes/magic.mjs";
import { UPGRADES } from "./hedgehog/scenes/upgrades.mjs";
import { DOWNGRADES } from "./hedgehog/scenes/downgrades.mjs";
import { INSTANTS } from "./hedgehog/scenes/instants.mjs";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");
const artDir = resolve(root, "public/themes/unhinged-hedgehogs/cards");
const catalogPath = resolve(root, "src/game/themes/unhingedHedgehogs.ts");

const SCENES = {
  ...BABIES,
  ...BASICS,
  ...MAGICALS,
  ...MAGICALS2,
  ...MAGIC,
  ...UPGRADES,
  ...DOWNGRADES,
  ...INSTANTS,
};

// Sanity: every named card must have a scene and vice versa.
const nameSlugs = Object.keys(HEDGEHOG_NAMES);
const sceneSlugs = Object.keys(SCENES);
const missingScene = nameSlugs.filter((s) => !SCENES[s]);
const orphanScene = sceneSlugs.filter((s) => !HEDGEHOG_NAMES[s]);
if (missingScene.length || orphanScene.length) {
  if (missingScene.length) console.error("Cards without art:", missingScene);
  if (orphanScene.length) console.error("Art without a card:", orphanScene);
  process.exit(1);
}

rmSync(artDir, { recursive: true, force: true });
mkdirSync(artDir, { recursive: true });
mkdirSync(dirname(catalogPath), { recursive: true });

for (const slug of nameSlugs) {
  writeFileSync(resolve(artDir, `${slug}.svg`), SCENES[slug].trimStart());
}

const entries = nameSlugs
  .map(
    (slug) =>
      `  ${JSON.stringify(slug)}: { name: ${JSON.stringify(HEDGEHOG_NAMES[slug])}, image: "/themes/unhinged-hedgehogs/cards/${slug}.svg" },`,
  )
  .join("\n");

const catalog = `// AUTO-GENERATED by scripts/gen-hedgehog-deck.mjs. Do not edit by hand.
// Original names and bespoke illustrations for the Unhinged Hedgehogs deck.

export interface HedgehogCardPresentation {
  name: string;
  image: string;
}

export const UNHINGED_HEDGEHOG_CARDS = {
${entries}
} as const satisfies Record<string, HedgehogCardPresentation>;
`;

writeFileSync(catalogPath, catalog);
try {
  execFileSync("vp", ["fmt", "--write", catalogPath], { stdio: "inherit" });
} catch {
  // formatter optional in non-dev environments
}
console.log(`Wrote ${nameSlugs.length} hedgehog illustrations and ${catalogPath}.`);
